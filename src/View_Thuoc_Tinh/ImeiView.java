    private void showName() {
        cbbTenSanPham.removeAllItems();
        for (Imei dl : repo.getNames()) {
            cbbTenSanPham.addItem(dl.getTen_San_Pham()); // Thêm Ten_San_Pham vào ComboBox
        }
    }

    private void showImei(ArrayList<Model.Imei> arr) {
        dtm = (DefaultTableModel) this.tblIemi.getModel();
        dtm.setRowCount(0);
        for (Model.Imei imei : arr) {
            Object[] row = {
                imei.getID_Imei(),
                imei.getMa_Imei(),
                imei.getTrang_thai() == 1 ? "Còn Hàng" : "Đã Bán",
                imei.getMa_San_Pham(),
                imei.getTen_San_Pham()
            };
            dtm.addRow(row);
        }
    }

    private void showImeiDB(ArrayList<Model.ImeiDaBan> arr) {
        dtm = (DefaultTableModel) this.tblIemi1.getModel();
        dtm.setRowCount(0);
        for (Model.ImeiDaBan imei : arr) {
            Object[] row = {
                imei.getID_Imei_Da_Ban(),
                imei.getMa_Imei_Da_Ban(),
                imei.getID_HDCT(),};
            dtm.addRow(row);
        }
    }

