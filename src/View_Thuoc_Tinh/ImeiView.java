    private void btnBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackActionPerformed
        // TODO add your handling code here:
        this.page--;
        this.showImei(repo.getAll(page, limit));
        this.lblPage.setText(this.page + "");

        // Nếu ở trang đầu tiên, vô hiệu hóa nút back, nếu không, kích hoạt nút next
        if (this.page == 1) {
            this.btnBack.setEnabled(false);
        } else {
            this.btnBack.setEnabled(true);
        }

        // Kích hoạt nút next khi không ở trang cuối
        if (this.page < 5) { // Giả sử trang cuối là 5
            this.btnNext.setEnabled(true);
        }
    }//GEN-LAST:event_btnBackActionPerformed

    private void btnNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNextActionPerformed
        // TODO add your handling code here:
        this.page++;
        this.showImei(repo.getAll(page, limit));
        this.lblPage.setText(this.page + "");

        // Kích hoạt nút back khi không ở trang đầu
        if (this.page > 1) {
            this.btnBack.setEnabled(true);
        }

        // Vô hiệu hóa nút next khi đến trang cuối
        if (this.page == 5) { // Giả sử trang cuối là 5
            this.btnNext.setEnabled(false);
        } else {
            this.btnNext.setEnabled(true);
        }
    }//GEN-LAST:event_btnNextActionPerformed

