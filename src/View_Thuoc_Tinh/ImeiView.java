    private void cbbTenSanPhamActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbbTenSanPhamActionPerformed
        // TODO add your handling code here:
        String ten = (String) cbbTenSanPham.getSelectedItem();
        dtm = (DefaultTableModel) this.tblIemi.getModel();
        dtm.setRowCount(0);
        for (Model.Imei imei : repo.SearchTen(page, limit, ten)) {
            Object[] row = {
                imei.getID_Imei(),
                imei.getMa_Imei(),
                imei.getTrang_thai() == 1 ? "Còn Hàng" : "Đã Bán",
                imei.getMa_San_Pham(),
                imei.getTen_San_Pham()
            };
            dtm.addRow(row);
        }
    }//GEN-LAST:event_cbbTenSanPhamActionPerformed

