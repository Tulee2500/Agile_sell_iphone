    private void tblIemiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblIemiMouseClicked
        // TODO add your handling code here:
        int row = tblIemi.getSelectedRow();
        if (row == -1) {
            return;
        }
        String IDImei = tblIemi.getValueAt(row, 0).toString();
        String Imei = tblIemi.getValueAt(row, 1).toString();
        String TrangThai = tblIemi.getValueAt(row, 2).toString();
        String MaSp = tblIemi.getValueAt(row, 3).toString();
        String TenSp = tblIemi.getValueAt(row, 4).toString();

        txtID_Imei.setText(IDImei);
        txtImei.setText(Imei);
        txtTrangThai.setText(TrangThai);
        txtMaSanPham.setText(MaSp);
        cbbTenSanPham.setSelectedItem(TenSp);
    }//GEN-LAST:event_tblIemiMouseClicked

