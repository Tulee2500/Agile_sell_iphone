    private void showPhanLoai(ArrayList<Model.PhanLoai> arr) {
        dtm = (DefaultTableModel) this.tblPhanLoai.getModel();
        dtm.setRowCount(0);
        for (Model.PhanLoai pl : arr) {
            Object[] row = {
                pl.getID_Phan_Loai(),
                pl.getPhan_Loai()
            };
            dtm.addRow(row);
        }
    }

