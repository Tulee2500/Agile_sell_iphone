    private void tblRamMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblRamMouseClicked
        // TODO add your handling code here:
        int row = this.tblRam.getSelectedRow();
        if (row == -1) {
            return; // Nếu không có hàng nào được chọn thì thoát
        }

// Lấy dữ liệu từ hàng được chọn
        String ID_String = this.tblRam.getValueAt(row, 0).toString(); // 0 là cột chứa ID
        int ID = Integer.parseInt(ID_String);
        String CPU = this.tblRam.getValueAt(row, 1).toString(); // 1 là cột chứa CPU

// Set giá trị cho các trường text
        this.txtRam.setText(CPU);
        this.txtID_Ram.setText(ID_String);
    }//GEN-LAST:event_tblRamMouseClicked

    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        String tenRam = txtRam.getText().trim();  // Sử dụng trim() để loại bỏ khoảng trắng đầu và cuối

        if (tenRam.isEmpty()) {
            // Kiểm tra nếu trường nhập trống
            JOptionPane.showMessageDialog(null, "Vui lòng nhập tên RAM.", "Thông báo", JOptionPane.WARNING_MESSAGE);
            return;  // Thoát khỏi phương thức nếu không có tên RAM
        }

        // Gọi phương thức insertinto từ repo để thêm tên RAM
        boolean isInserted = repo.insertInto(tenRam);  // Giả sử phương thức trả về true/false

        if (isInserted) {
            // Thông báo thêm thành công
            JOptionPane.showMessageDialog(null, "Thêm thành công tên RAM: " + tenRam, "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        } else {
            // Thông báo thêm thất bại
            JOptionPane.showMessageDialog(null, "Thêm thất bại. Tên RAM đã tồn tại hoặc xảy ra lỗi.", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }

        // Hiển thị danh sách RAM sau khi thêm
        this.showRam(repo.getAll());
        this.clear();
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        int ID;
        try {
            ID = Integer.parseInt(txtID_Ram.getText().trim());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "ID không hợp lệ.", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String tenRam = txtRam.getText().trim();  // Sử dụng trim() để loại bỏ khoảng trắng đầu và cuối

        if (tenRam.isEmpty()) {
            // Kiểm tra nếu trường nhập trống
            JOptionPane.showMessageDialog(null, "Vui lòng nhập tên RAM.", "Thông báo", JOptionPane.WARNING_MESSAGE);
            return;  // Thoát khỏi phương thức nếu không có tên RAM
        }

        // Gọi phương thức update từ repo để cập nhật tên RAM
        boolean isUpdated = repo.update(ID, tenRam);  // Giả sử phương thức trả về true/false

        if (isUpdated) {
            // Thông báo cập nhật thành công
            JOptionPane.showMessageDialog(null, "Cập nhật thành công tên RAM: " + tenRam, "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        } else {
            // Thông báo cập nhật thất bại
            JOptionPane.showMessageDialog(null, "Cập nhật thất bại. Tên RAM đã tồn tại hoặc xảy ra lỗi.", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }

        // Hiển thị danh sách RAM sau khi cập nhật
        this.showRam(repo.getAll());
        this.clear();
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        String tenRam = txtRam.getText().trim();  // Sử dụng trim() để loại bỏ khoảng trắng đầu và cuối

        if (tenRam.isEmpty()) {
            // Kiểm tra nếu trường nhập trống
            JOptionPane.showMessageDialog(null, "Vui lòng nhập tên RAM để xóa.", "Thông báo", JOptionPane.WARNING_MESSAGE);
            return;  // Thoát khỏi phương thức nếu không có tên RAM
        }

        // Gọi phương thức delete từ repo để xóa tên RAM
        boolean isDeleted = repo.delete(tenRam);  // Giả sử phương thức trả về true/false

        if (isDeleted) {
            // Thông báo xóa thành công
            JOptionPane.showMessageDialog(null, "Xóa thành công tên RAM: " + tenRam, "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        } else {
            // Thông báo xóa thất bại
            JOptionPane.showMessageDialog(null, "Xóa thất bại. Vui lòng kiểm tra lại.", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }

        // Hiển thị danh sách RAM sau khi xóa
        this.showRam(repo.getAll());
        this.clear();
    }//GEN-LAST:event_btnXoaActionPerformed

    private void txtSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchKeyReleased
        // TODO add your handling code here:
        String keyword = txtSearch.getText().trim();  // Lấy từ khóa tìm kiếm từ JTextField
        if (keyword.isEmpty()) {
            // Nếu không có từ khóa, hiển thị lại toàn bộ danh sách
            showRam(repo.getAll());
        } else {
            // Gọi phương thức tìm kiếm từ repo, trả về danh sách kết quả phù hợp
            ArrayList<Ram> result = repo.search(keyword);
            showRam(result);  // Hiển thị danh sách kết quả tìm kiếm
        }
    }//GEN-LAST:event_txtSearchKeyReleased
    public void clear() {
        this.txtRam.setText("");
        this.txtID_Ram.setText("");
    }

