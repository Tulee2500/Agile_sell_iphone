    private void showRam(ArrayList<Model.Ram> arr) {
        dtm = (DefaultTableModel) this.tblRam.getModel();
        dtm.setRowCount(0);
        for (Model.Ram ram : arr) {
            Object[] row = {
                ram.getID_Ram(),
                ram.getRam()
            };
            dtm.addRow(row);
        }
    }

