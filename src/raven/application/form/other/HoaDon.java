    public void fillTable(List<HoaDonModel> dsHoaDon) {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        SimpleDateFormat dateFormat = new SimpleDateFormat("dd-MM-yyyy");

        for (HoaDonModel hd : dsHoaDon) {
            String ngayTao = dateFormat.format(hd.getNgayTao());
            String ngayThanhToan = hd.getNgayThanhToan() != null ? dateFormat.format(hd.getNgayThanhToan()) : "";
            Object[] row = new Object[]{
                hd.getIdHoaDon(),
                hd.getMaHoaDon(),
                ngayTao,
                ngayThanhToan,
                hd.getTenKhachHang(),
                hd.getTenNhanVien(),
                hd.getTongGia(),
                hd.getTrangThai() == 0 ? "Chưa thanh toán" : "Đã thanh toán"
            };
            model.addRow(row);
        }
    }

