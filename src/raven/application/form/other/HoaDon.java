    private void btnTKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTKActionPerformed
        java.util.Date utilTuNgay = txtTuNgay.getDate();
        java.util.Date utilDenNgay = txtDenNgay.getDate();

        if (utilTuNgay == null || utilDenNgay == null) {
            JOptionPane.showMessageDialog(this, "Yêu cầu bạn nhập đầy đủ, không được để trống ngày bắt đầu và ngày kết thúc.");
            return;
        }

        if (utilTuNgay.after(utilDenNgay)) {
            JOptionPane.showMessageDialog(this, "Ngày kết thúc phải sau ngày bắt đầu!");
            return;
        }

        java.sql.Date sqlTuNgay = new java.sql.Date(utilTuNgay.getTime());
        java.sql.Date sqlDenNgay = new java.sql.Date(utilDenNgay.getTime());

        List<HoaDonModel> dsHoaDon = hoaDonRepository.getFilteredData(sqlTuNgay, sqlDenNgay);

        if (dsHoaDon.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không tìm thấy kết quả trong khoảng thời gian này.");
        } else {
            JOptionPane.showMessageDialog(this, "Đã tìm thấy kết quả thành công!");

            fillTable(dsHoaDon);
        }
    }//GEN-LAST:event_btnTKActionPerformed

